    private void CrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrearActionPerformed

        try {
            fila = Integer.parseInt(num.getText());

            DefaultTableModel modelo = new DefaultTableModel(fila, 2);
            matriz1.setModel(modelo);
            matriz1.setVisible(true);
            matriz1.setShowGrid(true);
            matriz = new double[fila][2];

            for (int i = 0; i < fila; i++) {
                for (int j = 0; j < 2; j++) {
                    matriz1.setValueAt("", i, j);

                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ingrese solo nÃºmeros");
            this.dispose();
            new Ventan().setVisible(true);
        }
    }//GEN-LAST:event_CrearActionPerformed

    private void ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarActionPerformed

        for (int i = 0; i < fila; i++) {
            for (int j = 0; j < 2; j++) {
                matriz[i][j] = Double.valueOf(matriz1.getValueAt(i, j).toString());
            }
        }
    }//GEN-LAST:event_ModificarActionPerformed

